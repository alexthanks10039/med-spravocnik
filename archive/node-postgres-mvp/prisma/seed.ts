async function main() {
  const password = await bcrypt.hash('Admin123!', 10);

  await prisma.user.upsert({
    where: { email: 'admin@med.local' },
    update: { password, role: Role.ADMIN },
    create: { email: 'admin@med.local', password, role: Role.ADMIN },
  });

  await prisma.$transaction([
    ...diseases.map(({ id, ...data }) =>
      prisma.disease.upsert({
        where: { id },
        update: data,
        create: { id, ...data },
      }),
    ),
    ...drugs.map(({ id, ...data }) =>
      prisma.drug.upsert({
        where: { id },
        update: data,
        create: { id, ...data },
      }),
    ),
    ...articles.map(({ slug, ...data }) =>
      prisma.article.upsert({
        where: { slug },
        update: data,
        create: { slug, ...data },
      }),
    ),
  ]);

  console.log(
    `Database seeded: 1 admin, ${diseases.length} diseases, ${drugs.length} drugs, ${articles.length} articles`,
  );
  console.log('Admin credentials: admin@med.local / Admin123!');
}

main()
  .catch((error) => {
    console.error(error);
    process.exitCode = 1;
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
